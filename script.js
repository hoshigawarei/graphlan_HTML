function showTooltip(evt, text) {
    const tooltip = document.getElementById('tooltip');
    tooltip.innerHTML = text;
    tooltip.style.display = 'block';
    tooltip.style.left = evt.pageX + 'px';
    tooltip.style.top = evt.pageY + 'px';
}

